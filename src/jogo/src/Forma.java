
public class Forma {

	private String nome = "";
	private String cor = "";
	private double id = 0.0;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public double getId() {
		return id;
	}
	public void setId(double id) {
		this.id = id;
	}
}
